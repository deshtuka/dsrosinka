                                    <b>Основная образовательная программа Муниципального бюджетного  дошкольного образовательного учреждения «Детского сада общеразвивающего вида №10 «Росинка» села Спасское</b> (далее - ООП ДО МБДОУ «ДС№10 «Росинка») представляет собой нормативный документ, внутренний стандарт ДОУ, разработанный в соответствии с ФГОС ДО, определяющий содержание дошкольного образования, а также   представляющий собой комплекс средств воспитания, обучения, оздоровления и развития детей, реализуемый на основе имеющихся ресурсов (кадровых и материальных) в соответствии с социальным заказом.<br><br>

                                    <b>Учебный план Муниципального бюджетного дошкольного образовательного учреждения «Детский сад общеразвивающего вида №10 «Росинка» села Спасское</b> (далее – ДОУ) разработан в соответствии с СП 2.4.3648-20 Санитарно-эпидемиологические требования к организациям воспитания и обучения, отдыха и оздоровления детей и молодежи утвержденные постановлением Главного государственного санитарного врача Российской Федерации от 28 сентября 2020 года N 28 ,  Уставом ДОУ, Приказом Министерства образования и науки Российской Федерации от 31.07.2020 №373  «Об утверждении порядка организации и осуществления образовательной деятельности по основным общеобразовательным программах - образовательным программам дошкольного образования».<br><br>

